package com.creatio.crm.language.basics;

/**
 * Assignment1-19-12-2024 - Data Types
 * This program explains the use of various data types in JAVA
 *
*/

public class DataTypesAssignment1 {

	public static void main(String[] args) {
		// 1. Temperature of a city in degrees Celsius: 25.5
		double cityTemp = 25.5; // double is used for Decimal values
		System.out.println("City Temperature : " + cityTemp + "Celsius");
		
		// 2. Whether a customer has placed an order: true or false
		boolean hasPlacedOrder = true; // boolean is used for true / false value
		System.out.println("Customer has placed an order : " + hasPlacedOrder);
		
		// 3. Person's phone number: "123-456-7890"
		String phoneNumber = "123-456-7890"; // String is used for text data
	    System.out.println("Person's Phone Number : " + phoneNumber);
		
	    // 4. Amount of money in a customer's bank account: 1000.50
	    double accountBalance = 1000.50; //double is used for currency values
	    System.out.println("Account Balance : " + accountBalance);
	    
	    // 5. Person's email address: "john.doe@example.com"
	    String emailAddress = "john.doe@2xample.com"; // String is s+used for text data
	    System.out.println("Email Address : " + emailAddress);
	    
	    // 6. Coordinates of a location (latitude, longitude): 37.7749, -122.4194
	    double lattitude = 37.7749; // decimal is used for precise decimal
	    double longitude = -122.4194;  // decimal is used for precise decimal
	    System.out.println("Location Coordinates : " + lattitude + "&" + longitude);
	    
	    // 7. Person's marital status: true or false
	    boolean maritalStatus = true;
	    System.out.println("Marital Status : " + maritalStatus);
	    
	    // 8. Person's occupation: "Software Engineer"
	    String personOccupation = "Software Engineer"; // String is s+used for text data
	    System.out.println("Person's Occupation : " + personOccupation);
	    
	    // 9. Person's Favorite color: "Blue"
	    String favouriteColor = "Blue"; // String is s+used for text data
	    System.out.println("Person's Favourite Colour : " +favouriteColor);
	    
	    // 10.Current year: 2023
	    int currentYear = 2023;
	    System.out.println("Current Year : " + currentYear);
	    
	    // 11.Number of followers on a social media platform: 1,000,000
	    String numberOfFollowers = "1,000,000"; // String is s+used for text data
	    System.out.println("Number Of Followers on a social media platform:" + numberOfFollowers);
	    
	    // 12.Rating of a movie: 7.5
	    float ratingOfMovie = 7.5f; 
	    System.out.println("Rating Of a Movie : " + ratingOfMovie);
	    
	    // 13.Person's blood type: 'A'
	    char bloodType = 'A'; //char is used for single character, it should be in single quotes
	    System.out.println("Blood Type of the Person : " + bloodType);
	    
	    // 14.Title of a book: "To Kill a Mockingbird"
	    String titleOfBook = "To Kill a Mockingbird"; // String is s+used for text data
	    System.out.println("Title of a Book : " + titleOfBook);
	    
	    // 15.Number of employees in a company: 500
	    int numberOfEmployees = 500; // int is used for whole numbers
	    System.out.println("Number Of Employees in a company : " + numberOfEmployees);
	    
	    // 16.Time of an event: 2:30 PM
	    String timeOfEvent = "2:30 PM"; // String is s+used for text data
	    System.out.println("Time of an event : " + timeOfEvent);
	    
	    // 17.Name of a country: "United States"
	    String nameOfCountry = "United States"; // String is s+used for text data
	    System.out.println("Name of a country : " + nameOfCountry);
	    
	    // 18.Person's eye color: "Brown"
	    String eyeColour = "Brown"; // String is s+used for text data
	    System.out.println("Person's eye color : " + eyeColour);
	    
	    // 19.Person's birthplace: "New York City"
	    String birthPlace = "New York City"; // String is s+used for text data
	    System.out.println("Person's Birth Place : " + birthPlace);
	    
	    // 20. Distance between two cities: 200.5
	    double distanceBtw2Cities = 200.5; //double is used for currency value
	    System.out.println("Distance between two cities : " + distanceBtw2Cities);
		
		

	}

}
